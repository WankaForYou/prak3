package prak3;

public class Hand {
    boolean writing_hand;

    public Hand(boolean writing_hand)
    {
        this.writing_hand = writing_hand;
    }

    public void setWriting_hand(boolean writing_hand)
    {
        this.writing_hand = writing_hand;
    }

    public boolean isWriting_hand()
    {
        return writing_hand;
    }
}
